     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $empresas = new Empresa();
        $empresas->nombrempresa = $request->get('nombrempresa');
        $empresas->direccion = $request->get('direccion');
        $empresas->telefono = $request->get('telefono');
        $empresas->correo = $request->get('correo');
        $empresas->kit = $request->get('kit');
        $empresas->personacontac = $request->get('personacontac');
        $empresas->contraseña = $request->get('contraseña');

        $empresas->save();

        return redirect('/empresas');
    }

